with open("FIPS6.txt", "r") as fips:
    file = fips.read().split("\n")[1:]
    m_obj = {}
    for row in file:
        if len(row)>1:
            data = row.split("\t")
            state = data[0]
            fips_code = data[1]
            county = data[3]
            m_obj[fips_code] = {"state":states[state], "county":county}
    with open("fips.json", "w") as fjs:
        json.dump(m_obj,fjs)


